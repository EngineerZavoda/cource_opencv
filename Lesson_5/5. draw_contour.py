# Рисование всех контуров

result = img.copy()             # Делаем копию исходного изображения, чтобы не портить оригинал
cv2.drawContours(result,        # Рисуем контуры на копии изображения
                 contours,      # Список контуров, которые будем отрисовывать
                 -1,            # Индекс -1 означает: рисовать все контуры сразу
                 (0, 255, 0),   # Цвет линии в формате BGR: ярко-зеленый
                 2)             # Толщина линии контура в пикселях

